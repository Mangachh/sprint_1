package tasca6.nivell1.Exercici1;

public class NoGenericMethods {
    
    private Integer numA;
    private Integer numB;
    private Integer numC;

    public NoGenericMethods(Integer numA, Integer numB, Integer numC) {
        this.numA = numA;
        this.numB = numB;
        this.numC = numC;
    }

    public Integer getNumA() {
        return numA;
    }

    public void setNumA(Integer numA) {
        this.numA = numA;
    }

    public Integer getNumB() {
        return numB;
    }

    public void setNumB(Integer numB) {
        this.numB = numB;
    }

    public Integer getNumC() {
        return numC;
    }

    public void setNumC(Integer numC) {
        this.numC = numC;
    }

    
    
}
